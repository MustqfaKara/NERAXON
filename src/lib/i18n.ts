const sortedEnglishPhrases = [...englishPhrases].sort((left, right) => right[0].length - left[0].length);
const englishTranslations = new Map(sortedEnglishPhrases);
const englishPattern = new RegExp(sortedEnglishPhrases.map(([source]) => escapePattern(source)).join("|"), "g");

export function translateText(value: string, language: AppLanguage) {
  if (language === "tr" || !value.trim()) return value;
  const translated = value.replace(englishPattern, (source) => englishTranslations.get(source) ?? source);
  const hasTurkishChainNumbers = /Blok:|Gas maliyeti:|Native değer:|Token hareketleri:/.test(value);
  if (!hasTurkishChainNumbers) return translated;
  return translated
    .replace(/\b\d+,\d+\b/g, (number) => number.replace(",", "."))
    .replace(/\b\d{1,3}(?:\.\d{3})+\b/g, (number) => number.replaceAll(".", ","));
}

export const localeFor = (language: AppLanguage) => language === "en" ? "en-US" : "tr-TR";

function escapePattern(value: string) {
  return value.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
}
